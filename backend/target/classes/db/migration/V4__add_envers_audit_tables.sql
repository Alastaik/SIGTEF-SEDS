-- V4__add_envers_audit_tables.sql
-- Creates the standard Hibernate Envers REVINFO and _AUD tables for auditing

-- Default REVINFO table
CREATE TABLE revinfo (
    rev INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    revtstmp BIGINT
);

-- Users Audit Table
CREATE TABLE users_aud (
    id UUID NOT NULL,
    rev INTEGER NOT NULL,
    revtype SMALLINT,
    name VARCHAR(255),
    email VARCHAR(255),
    password VARCHAR(255),
    user_type VARCHAR(50),
    active BOOLEAN,
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    failed_login_attempts INT,
    locked_until TIMESTAMP,
    reset_token VARCHAR(255),
    reset_token_expires_at TIMESTAMP,
    PRIMARY KEY (id, rev),
    CONSTRAINT fk_users_aud_revinfo FOREIGN KEY (rev) REFERENCES revinfo (rev)
);

-- Roles Audit Table
CREATE TABLE roles_aud (
    id UUID NOT NULL,
    rev INTEGER NOT NULL,
    revtype SMALLINT,
    name VARCHAR(100),
    description TEXT,
    PRIMARY KEY (id, rev),
    CONSTRAINT fk_roles_aud_revinfo FOREIGN KEY (rev) REFERENCES revinfo (rev)
);

-- Permissions Audit Table
CREATE TABLE permissions_aud (
    id UUID NOT NULL,
    rev INTEGER NOT NULL,
    revtype SMALLINT,
    name VARCHAR(100),
    description TEXT,
    PRIMARY KEY (id, rev),
    CONSTRAINT fk_permissions_aud_revinfo FOREIGN KEY (rev) REFERENCES revinfo (rev)
);

-- User Roles Join Table Audit
CREATE TABLE user_roles_aud (
    rev INTEGER NOT NULL,
    user_id UUID NOT NULL,
    role_id UUID NOT NULL,
    revtype SMALLINT,
    PRIMARY KEY (user_id, role_id, rev),
    CONSTRAINT fk_user_roles_aud_revinfo FOREIGN KEY (rev) REFERENCES revinfo (rev)
);

-- Role Permissions Join Table Audit
CREATE TABLE role_permissions_aud (
    rev INTEGER NOT NULL,
    role_id UUID NOT NULL,
    permission_id UUID NOT NULL,
    revtype SMALLINT,
    PRIMARY KEY (role_id, permission_id, rev),
    CONSTRAINT fk_role_permissions_aud_revinfo FOREIGN KEY (rev) REFERENCES revinfo (rev)
);

-- User Entity Scope Audit Table
CREATE TABLE user_entity_scopes_aud (
    id UUID NOT NULL,
    rev INTEGER NOT NULL,
    revtype SMALLINT,
    user_id UUID,
    entity_id UUID,
    created_at TIMESTAMP,
    PRIMARY KEY (id, rev),
    CONSTRAINT fk_user_entity_scopes_aud_revinfo FOREIGN KEY (rev) REFERENCES revinfo (rev)
);
